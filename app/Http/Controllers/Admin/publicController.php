     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate(
            ['url' => 'required|image|max:2048'],
            ['descripcion' => 'required|min:1|max:250']
        );

        $img = $request->file('url')->store('public/images');
        $endl = Storage::url($img);
        $desc = $request->input('descripcion');


        publicacione::create([
            'url' => $endl,
            'descripcion' => $desc
        ]);

        return redirect()->route('admin.publicacion.index');
    }

